/*
Exercício 7: Transformação com map()
Crie um array de números [1, 2, 3, 4]. Use o método map() para criar um novo array em que cada número seja multiplicado por 5. Exiba o novo array no console.
*/

let numeros = [1, 2, 3, 4];

let multiplicado5 = numeros.map(n => n * 5);

console.log(multiplicado5);
